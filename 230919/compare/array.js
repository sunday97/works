// 포켓몬 배열에 이상해씨를 추가해주세요
pokemon.push(`이상해씨`)

// 배열의 '마지막 요소를 제거'
pokemon.pop();

console.log(pokemon)

// 배열.length : 배열의 길이를 알아 보는 방법
console.log(pokemon.length)
// 배열의 요소를 셀 때는 0부터 시작합니다.
